const firstNumber = 10;
const secondNumber = 5;

if (firstNumber > secondNumber) {
  console.log(
    `FirstNumber ${firstNumber} is bigger than SecondNumber ${secondNumber}`
  );
}

if (firstNumber <= secondNumber) {
  console.log(
    `FirstNumber ${firstNumber} is lower or equal to SecondNumber ${secondNumber}`
  );
}

